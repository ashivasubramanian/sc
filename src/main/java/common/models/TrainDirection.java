package common.models;

/**
 * Enum that represents the possible directions a train can take on the section.
 */
public enum TrainDirection {

    /**
     * A train with direction TOWARDS_HOME is travelling towards Calicut. <br>The direction is
     * set using the <code>Train</code> constructor.
     */
    TOWARDS_HOME,

    /**
     * A train with direction AWAY_FROM_HOME is travelling towards Shoranur Junction. <br>The
     * direction is set using the <code>Train</code> constructor.
     */
    AWAY_FROM_HOME

}
